


# funciones para convertir datos


# Función str()

# La función str() convierte un dato a string. Por ejemplo, si queremos convertir un número a string, podemos hacerlo de la siguiente manera:

numero = 20
print( type(numero) )


numero = str(numero)

print("Ahora la variable numero es de tipo: ", type(numero) )

# Función int()

# Función int() convierte un dato a entero. Por ejemplo, si queremos convertir un string a entero, podemos hacerlo de la siguiente manera:

numero = "20"
numero = int("654")

print("----- Usamos la función int() -----")
print(type(numero))



# Función float()

# La función float() convierte un dato a float. Por ejemplo, si queremos convertir un string a float, podemos hacerlo de la siguiente manera:

print( float("20.3") )

